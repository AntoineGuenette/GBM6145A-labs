function update_non_EMG_results(file_path)

arguments
    file_path (1,1) string
end

% Load subject info
subjects = get_subject_info();

% Load data
C = readcell(file_path);

% Process each subject's data
for i = 1:length(subjects)
    col = i+1;
    
    % -- Task group 2 --

    % HFT Large Light Object (time)
    HFT_LLO_diff = compute_non_EMG_diff( ...
        subjects(i).HFT_LLWObj_1_JAECO_s, ...
        subjects(i).HFT_LLWObj_2_JAECO_s, ...
        subjects(i).HFT_LLWObj_3_DynAReach_s, ...
        subjects(i).HFT_LLWObj_4_DynAReach_s, ...
        2);
    row = find(strcmp(C(:,1), 'HFT_LLO-time'));
    C{row, col} = HFT_LLO_diff;

    % Joystick (time)
    joystick_diff = compute_non_EMG_diff( ...
        subjects(i).joystick_1_JAECO_s, ...
        subjects(i).joystick_2_JAECO_s, ...
        subjects(i).joystick_3_DynAReach_s, ...
        subjects(i).joystick_4_DynAReach_s, ...
        2);
    row = find(strcmp(C(:,1), 'Joystick-time'));
    C{row, col} = joystick_diff;

    % -- Task group 3 --

    % JAMAR (force)
    Jamar_diff = compute_non_EMG_diff( ...
        subjects(i).JAMAR_1_JAECO_kg, ...
        subjects(i).JAMAR_2_JAECO_kg, ...
        subjects(i).JAMAR_3_JAECO_kg, ...
        subjects(i).JAMAR_4_DynAReach_kg, ...
        3);
    row = find(strcmp(C(:,1), 'Jamar-force'));
    C{row, col} = Jamar_diff;

    % HFT spoon (time)
    HFT_spoon_diff = compute_non_EMG_diff( ...
        subjects(i).HFT_spoon_1_JAECO_s, ...
        subjects(i).HFT_spoon_2_JAECO_s, ...
        subjects(i).HFT_spoon_3_JAECO_s, ...
        subjects(i).HFT_spoon_4_DynAReach_s, ...
        3);
    row = find(strcmp(C(:,1), 'HFT_spoon-time'));
    C{row, col} = HFT_spoon_diff;

    % BBT (number of blocks)
    BBT_diff = compute_non_EMG_diff( ...
        subjects(i).BBT_1_JAECO, ...
        subjects(i).BBT_2_JAECO, ...
        subjects(i).BBT_3_JAECO, ...
        subjects(i).BBT_4_DynAReach, ...
        3);
    row = find(strcmp(C(:,1), 'BBT-nb_blocks'));
    C{row, col} = BBT_diff;

end

% Write data
writecell(C, file_path);

end
